  async function loadMyBlogs(container) {
    var posts = await TobUtils.withToast(
      supabase.from('posts')
        .select('id, title, content, image_url, created_at, author_id')
        .eq('author_id', currentProfile.id)
        .order('created_at', { ascending: false }),
      'Could not load your blogs.'
    );

    if (!posts || !posts.length) {
      container.innerHTML =
        '<div class="empty-state">' +
          '<svg xmlns="http://www.w3.org/2000/svg" width="48" height="48" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="1.5" stroke-linecap="round" stroke-linejoin="round"><path d="M14 2H6a2 2 0 0 0-2 2v16a2 2 0 0 0 2 2h12a2 2 0 0 0 2-2V8z"/><polyline points="14 2 14 8 20 8"/></svg>' +
          '<p>No blogs yet. Click the compose button in the navbar to write your first blog.</p>' +
        '</div>';
      return;
    }

    var ids        = posts.map(function (p) { return p.id; });
    var likedIds   = await Interactions.getUserInteractions('likes', ids, currentUser.id);
    var starredIds = await Interactions.getUserInteractions('stars', ids, currentUser.id);

    container.innerHTML = '';
    posts.forEach(function (post) {
      var cardPost = Object.assign({}, post, { profiles: { username: currentProfile.username, display_name: currentProfile.display_name || null, profile_image_url: currentProfile.profile_image_url } });
      container.appendChild(BlogCard.create(cardPost, {
        currentUserId: currentUser.id,
        likedIds:      likedIds,
        starredIds:    starredIds,
        showMenu:      true,
        onEdit:        function (p) { Nav.openComposeModal(p); },
        onDelete:      function (id) {
          deletePostId = id;
          document.getElementById('delete-modal').classList.remove('hidden');
        },
      }));
    });
  }

